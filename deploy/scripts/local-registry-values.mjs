/**
 * Harbor image paths for local GitOps — derived from deploy/pinned-images.env.
 * Rendered to per-chart values-local-registry.yaml via render-local-registry-values.mjs.
 */
import { readPinnedImages, requirePinnedImage } from "./k8s-local-common.mjs";
import { resolveLocalRegistryConfig } from "./k8s-local-registry-config.mjs";

function splitImageRef(image) {
  const normalized = image
    .replace(/^docker\.io\//, "")
    .replace(/^registry-1\.docker\.io\//, "");
  const tagIdx = normalized.lastIndexOf(":");
  if (tagIdx === -1 || tagIdx < normalized.lastIndexOf("/")) {
    return { repository: normalized, tag: "latest" };
  }
  return {
    repository: normalized.slice(0, tagIdx),
    tag: normalized.slice(tagIdx + 1),
  };
}

/** Full image ref mirrored under Harbor project (Hatchet, Keycloak, busybox). */
function harborImageRef(localRegistry, image) {
  const { repository, tag } = splitImageRef(image);
  return {
    repository: `${localRegistry}/${repository}`,
    tag,
  };
}

/** Bitnami-style repo+tag; global.imageRegistry in values-local.yaml adds Harbor prefix. */
function bitnamiImageRef(image) {
  return splitImageRef(image);
}

function harborImageString(localRegistry, image) {
  const { repository, tag } = harborImageRef(localRegistry, image);
  return `${repository}:${tag}`;
}

/** @param {string} root */
export function buildLocalRegistryChartValues(root) {
  const pinned = readPinnedImages(root);
  const { localRegistry } = resolveLocalRegistryConfig(root);

  const hatchetApi = harborImageRef(
    localRegistry,
    requirePinnedImage(pinned, "REPODY_HATCHET_API_IMAGE"),
  );
  const hatchetMigrate = harborImageRef(
    localRegistry,
    requirePinnedImage(pinned, "REPODY_HATCHET_MIGRATE_IMAGE"),
  );
  const hatchetAdmin = harborImageRef(
    localRegistry,
    requirePinnedImage(pinned, "REPODY_HATCHET_ADMIN_IMAGE"),
  );
  const hatchetEngine = harborImageRef(
    localRegistry,
    requirePinnedImage(pinned, "REPODY_HATCHET_ENGINE_IMAGE"),
  );
  const hatchetFrontend = harborImageRef(
    localRegistry,
    requirePinnedImage(pinned, "REPODY_HATCHET_FRONTEND_IMAGE"),
  );
  const hatchetPostgres = bitnamiImageRef(
    requirePinnedImage(pinned, "REPODY_BITNAMI_POSTGRES_IMAGE"),
  );
  const hatchetPostgresInit = harborImageRef(
    localRegistry,
    requirePinnedImage(pinned, "REPODY_POSTGRES_IMAGE"),
  );
  const hatchetRabbitmq = bitnamiImageRef(
    requirePinnedImage(pinned, "REPODY_BITNAMI_RABBITMQ_IMAGE"),
  );
  const busybox = harborImageString(
    localRegistry,
    requirePinnedImage(pinned, "REPODY_BUSYBOX_IMAGE"),
  );
  const keycloak = harborImageString(
    localRegistry,
    requirePinnedImage(pinned, "REPODY_KEYCLOAK_IMAGE"),
  );

  const hatchetStack = {
    sharedConfig: {
      image: { tag: hatchetApi.tag },
    },
    api: {
      image: { repository: hatchetApi.repository, tag: hatchetApi.tag },
      migrationJob: {
        image: { repository: hatchetMigrate.repository, tag: hatchetMigrate.tag },
      },
      setupJob: {
        image: { repository: hatchetAdmin.repository, tag: hatchetAdmin.tag },
      },
      postgresImage: {
        repository: hatchetPostgresInit.repository,
        tag: hatchetPostgresInit.tag,
      },
    },
    engine: {
      image: { repository: hatchetEngine.repository, tag: hatchetEngine.tag },
    },
    frontend: {
      image: { repository: hatchetFrontend.repository, tag: hatchetFrontend.tag },
    },
    postgres: {
      image: { repository: hatchetPostgres.repository, tag: hatchetPostgres.tag },
    },
    rabbitmq: {
      image: { repository: hatchetRabbitmq.repository, tag: hatchetRabbitmq.tag },
    },
  };

  return {
    queue: {
      hatchet: { waitInitImage: busybox },
      "hatchet-stack": hatchetStack,
    },
    auth: {
      keycloak: { image: keycloak },
    },
    data: {},
    app: {
      hatchet: { waitInitImage: busybox },
    },
    localRegistry,
  };
}

function yamlScalar(value) {
  if (/[:#{}[\],&*?]|^\s/.test(value) || value === "") {
    return JSON.stringify(value);
  }
  return value;
}

function yamlLines(obj, indent = 0) {
  const pad = "  ".repeat(indent);
  const lines = [];
  for (const [key, value] of Object.entries(obj)) {
    if (value === null || value === undefined) continue;
    if (typeof value === "object" && !Array.isArray(value)) {
      lines.push(`${pad}${key}:`);
      lines.push(...yamlLines(value, indent + 1));
    } else {
      lines.push(`${pad}${key}: ${yamlScalar(String(value))}`);
    }
  }
  return lines;
}

/** @param {string} root @param {"queue"|"auth"|"data"|"app"} chart */
export function renderLocalRegistryValuesYaml(root, chart) {
  const built = buildLocalRegistryChartValues(root);
  const header = `# Generated by: node deploy/scripts/render-local-registry-values.mjs
# Source: deploy/pinned-images.env + Harbor registry (${built.localRegistry})
# Warm Harbor before sync: pnpm registry:warm
`;
  return `${header}${yamlLines(built[chart]).join("\n")}\n`;
}
